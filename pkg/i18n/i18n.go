// GetMessage retrieves a localized message by code with optional format arguments
func GetMessage(code string, args ...interface{}) string {
	msg, ok := ErrorMessagesAr[code]
	if !ok {
		return code
	}
	if len(args) > 0 {
		return fmt.Sprintf(msg, args...)
	}
	return msg
}

// GetHint retrieves the hint for a given error code
func GetHint(code string) string {
	hintCode := code + "_HINT"
	if hint, ok := ErrorMessagesAr[hintCode]; ok {
		return hint
	}
	return ""
}
